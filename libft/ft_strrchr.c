#include "libft.h"
#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	char	*str;

	str = (char *)s;
	while (*str)
		str++;
	while ((*str) != c && (*str) != s[0])
		str--;
	if (*str == c)
		return (str);
	else
		return (NULL);
}
